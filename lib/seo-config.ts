// Utility function to get meta tags for a specific page
export const getMetaTagsForPage = (pagePath: string) => {
  return SEO_CONFIG.contentRecommendations.find((rec) => rec.page === pagePath) || null
}

// Utility function to get keyword cluster details
export const getKeywordCluster = (cluster: string) => {
  const keywords = SEO_CONFIG.keywords as Record<string, any>
  return keywords[cluster] || null
}

// Utility function to get service earning details
export const getServiceEarnings = (serviceName: string) => {
  const services = SEO_CONFIG.services as Record<string, any>
  return services[serviceName]?.earnings || null
}

// Utility function to generate schema markup
export const generateFAQSchema = () => {
  return {
    "@context": "https://schema.org",
    "@type": "FAQPage",
    mainEntity: SEO_CONFIG.schemaRecommendations.faqSchema,
  }
}

// Utility function to get all services for breadcrumb navigation
export const getAllServices = () => {
  return Object.keys(SEO_CONFIG.services).map((key) => ({
    id: key,
    title: SEO_CONFIG.services[key as keyof typeof SEO_CONFIG.services].title,
  }))
}
